package com;

public class Procesador {
	
	String marca;
	String modelo;
	double velocidad;
	int nucleos;
	double cache;
	
	
	public Procesador() {
		
	}


	public Procesador(String marca, String modelo, double velocidad, int nucleos, double cache) {
		super();
		this.marca = marca;
		this.modelo = modelo;
		this.velocidad = velocidad;
		this.nucleos = nucleos;
		this.cache = cache;
	}
	
	

}

